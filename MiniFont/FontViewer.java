	public void createImage() {
		//16*16
		BufferedImage image = new BufferedImage( 16 * 4, 16 * 6,BufferedImage.TYPE_BYTE_BINARY);
		int size = minifont_data.length;
		int paintX = 0, paintY = 0;
		
		Graphics2D g2d = (Graphics2D )image.getGraphics();  
		g2d.setColor(Color.GREEN); 
		g2d.fillRect(0, 0, 16 * 4, 16 * 6);
		g2d.dispose();
		
		for(int cur = 0, k = 0; cur < size; cur += 6, k++) {
			paintY = (cur / 96) * 6;
			
			for(int i = cur; i < cur + 6; i++) {
				paintX = (k == 16) ? 0 : k * 4;
				k = (k == 16) ? 0 : k;
				for(int j = 0; j < 4; j++) {
					try {
						byte[] byteArray = String.valueOf(minifont_data[i]).getBytes("UTF-8");
						image.setRGB(paintX++, paintY, (((byteArray[0]>>j) & 1) == 1) ? 0xFF000000 : 0xFFFFFFFF);
					} catch (UnsupportedEncodingException e) {
						e.printStackTrace();
					}
				}
				paintY++;
			}
		}
		
		File file = new File(System.getProperty("user.dir")+"\\output.png");
		if(file.exists()) {
			file.delete();
		}
		
		try {
			file.createNewFile();
		} catch (IOException e) {
			System.out.println(e.getMessage());
		}
		
		try {
			ImageIO.write(image, "png", file);
		} catch (IOException e) {
			e.printStackTrace();
		}
	}
